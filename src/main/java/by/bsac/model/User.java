package by.bsac.model;

public class User {

    private String user_name; //User name.
    private String user_mail; //User mail.
    private String user_pass; //User password.

    //Constructor
    public User(String user_name, String user_mail, String user_pass) {
        this.user_name = user_name;
        this.user_mail = user_mail;
        this.user_pass = user_pass;
    }




}

